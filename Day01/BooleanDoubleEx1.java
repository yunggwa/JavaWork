package Day01;

public class BooleanDoubleEx1 {
    public static void main(String[] args) {
        boolean isMarried = true;
        System.out.println(isMarried);

        double dNum = 3.14;
        float fNum = 3.14F; //필수로 리트럴 넣어줘야한다

        System.out.println(dNum);
        System.out.println(fNum);
    }
}
